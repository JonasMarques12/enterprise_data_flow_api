     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        if (Group::where('group_code', $request->group["group_code"])->exists()) {
            return ['errorMessage' =>"Grupo jÃ¡ existe!"];
        } else {
            
            $newGroup = new Group;
            $newGroup->group_code = $request->group["group_code"];
            $newGroup->group_name = $request->group["group_name"];
            $newGroup->group_description = $request->group["group_description"];
            $newGroup->save();
            return "Grupo cadastrado com sucesso!";
        }
    }

